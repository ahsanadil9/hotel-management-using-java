    public void roomCategory() 
     {

        try {      
                pst = con.prepareStatement("select Distinct Rcat from roomTble"); 
                ResultSet rs = pst.executeQuery();
                Ccat.removeAllItems();
                while(rs.next())
                {
                    Ccat.addItem(rs.getString("Rcat"));
                }
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }      
    }        
    
    
