    public void show_CustomerTble1()                                    
        {
        try {
            pst = con.prepareStatement("select * from CustomerTble1");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();    
            d = (DefaultTableModel)CustomerTble1.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v2 = new Vector();
                for (int i=1; i<=c; i++)
                {                   
                    v2.add(rs.getString("Ccheckin"));
                    v2.add(rs.getString("Ccheckout"));
                    v2.add(rs.getString("Ccat"));
                    v2.add(rs.getString("Croom"));
                    v2.add(rs.getString("Cprice"));
                }                    
                    d.addRow(v2);                
            }           
        } catch (SQLException ex) {
            Logger.getLogger(InfoUnchangable.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        }     
