        //displaying output in our table 
    public void show_CustomerTble()                                    
        {
        try {
            pst = con.prepareStatement("select * from CustomerTble");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();    
            d = (DefaultTableModel)CustomerTble.getModel();
            d.setRowCount(0);
            while(rs.next())
            {
                Vector v2 = new Vector();
                for (int i=1; i<=c; i++)
                {
                    v2.add(rs.getString("Cr"));
                    v2.add(rs.getString("Cfname"));
                    v2.add(rs.getString("Cgen"));
                    v2.add(rs.getString("Ccont"));
                    v2.add(rs.getString("Caddress"));
                    v2.add(rs.getString("Ccity"));
                    v2.add(rs.getString("Ccnic"));
                   
                }                    
                    d.addRow(v2);                
            }           
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        }     
