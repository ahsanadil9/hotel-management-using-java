    public void autoId()
    {
        try {
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery("select MAX(Cr) from customerTble");
            rs.next();
            rs.getString("MAX(Cr)");
            if(rs.getString("MAX(Cr)") ==  null)
            {
                Cr.setText("M0001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("MAX(Cr)").substring(1, rs.getString("MAX(Cr)").length()));
                id++;
                Cr.setText("M0" + String.format("%03d", id)); // add "%03d" to add 3 leading zeros in an Integer.
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
